import { execFileSync } from 'node:child_process'
import { mkdirSync, readdirSync, statSync, writeFileSync } from 'node:fs'
import { dirname, join, relative, resolve, sep } from 'node:path'

const rootDir = process.cwd()
const contentDir = join(rootDir, 'content')
const guidesDir = join(contentDir, 'guides')
const outputFile = join(rootDir, 'app', 'generated', 'guide-updated.ts')

const toPosixPath = (value) => value.replace(/\\/g, '/')

const formatDate = (value) => value.toISOString().slice(0, 10)

const ensurePathWithin = (baseDir, targetPath) => {
  const resolvedBase = resolve(baseDir)
  const resolvedTarget = resolve(targetPath)
  const basePrefix = resolvedBase.endsWith(sep) ? resolvedBase : `${resolvedBase}${sep}`
  if (resolvedTarget !== resolvedBase && !resolvedTarget.startsWith(basePrefix)) {
    throw new Error(`Unsafe path outside root: ${resolvedTarget}`)
  }

  return resolvedTarget
}

const safeReaddirSync = (directory) => {
  const safeDirectory = ensurePathWithin(guidesDir, directory)
  // eslint-disable-next-line security/detect-non-literal-fs-filename
  return readdirSync(safeDirectory, { withFileTypes: true })
}

const safeStatSync = (filePath) => {
  const safeFilePath = ensurePathWithin(guidesDir, filePath)
  // eslint-disable-next-line security/detect-non-literal-fs-filename
  return statSync(safeFilePath)
}

const readMarkdownFiles = (directory) => {
  const safeDirectory = ensurePathWithin(guidesDir, directory)
  const files = []

  for (const entry of safeReaddirSync(safeDirectory)) {
    const entryPath = ensurePathWithin(guidesDir, join(safeDirectory, entry.name))

    if (entry.isDirectory()) {
      files.push(...readMarkdownFiles(entryPath))
      continue
    }

    if (entry.isFile() && entry.name.endsWith('.md')) {
      files.push(entryPath)
    }
  }

  return files
}

const getGitUpdatedDate = (filePath) => {
  const fileRelativeToRepo = toPosixPath(relative(rootDir, filePath))

  try {
    const output = execFileSync(
      'git',
      ['log', '-1', '--format=%cs', '--', fileRelativeToRepo],
      { cwd: rootDir, encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'] },
    ).trim()

    if (/^\d{4}-\d{2}-\d{2}$/.test(output)) {
      return output
    }
  } catch {
    // Ignore git lookup failures and fallback to mtime.
  }

  return null
}

const getRoutePath = (filePath) => {
  const relativeToContent = toPosixPath(relative(contentDir, filePath))
  return `/${relativeToContent.replace(/\.md$/, '')}`
}

const markdownFiles = readMarkdownFiles(guidesDir)
const updatedByPath = Object.fromEntries(
  markdownFiles
    .map((filePath) => {
      const updatedAt = getGitUpdatedDate(filePath) ?? formatDate(safeStatSync(filePath).mtime)
      return [getRoutePath(filePath), updatedAt]
    })
    .sort((a, b) => a[0].localeCompare(b[0])),
)

const outputLines = [
  '// Auto-generated by scripts/generate-guide-updated.mjs',
  '// Do not edit this file manually.',
  '',
  'export const GUIDE_UPDATED_BY_PATH: Record<string, string> = {',
  ...Object.entries(updatedByPath).map(([routePath, updatedAt]) => `  ${JSON.stringify(routePath)}: ${JSON.stringify(updatedAt)},`),
  '}',
  '',
]

mkdirSync(dirname(outputFile), { recursive: true })
writeFileSync(outputFile, outputLines.join('\n'))
